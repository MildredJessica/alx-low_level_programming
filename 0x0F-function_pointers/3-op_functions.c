#include "3-calc.h"
#include <stdio.h>

/**
 * op_add - Adds two numbers
 * @a: Parameter
 * @b: Parameter
 * Return: integer
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtracts two numbers
 * @a: Parameter
 * @b: Parameter
 * Return: integer
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplies two numbers
 * @a: Parameter
 * @b: Parameter
 * Return: integer
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - division of two numbers
 * @a: Parameter
 * @b: Parameter
 * Return: integer
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - modulus of two numbers
 * @a: Parameter
 * @b: Parameter
 * Return: integer
 */
int op_mod(int a, int b)
{
	return (a % b);
}
